コーディング規則
読みやすいコードを書くために Kotlin スタイルガイド というものが用意されています。

Android アプリ開発をするプログラマが守るべき 「Kotlin コードの書き方に関するルール」 のようなもので、Google も推奨しているものです。

正しいコードを記述できるように、早い段階から少しずつ慣れておきましょう。

ここでは基本的なルールだけ紹介するので、プログラミングに慣れてきたらKotlin スタイルガイドもぜひ読んでみてください。


