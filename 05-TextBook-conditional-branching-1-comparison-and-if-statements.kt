// if - else 文
//「条件に一致した場合、しなかった場合」の２通りのコードを書きたいこともあります。
// そのときに使うのが if - else 文です。
// if (条件) {
//    条件に一致したら実行するコード
// } else {
//    条件に一致しなかったときに実行するコード
// }

fun main() {
    val score = 99

    if (score == 100) {
        println("大吉")
    } else {
        println("吉")
    }
}

// if - else if - else 文
// if - else 文に、さらに条件を追加できるのが、 if - else if - else 文です。
// 一番最後の else は、実行するコードがないときは省略できます。
// if (条件１) {

// } else if (条件２) {

// } else if (条件３) {
    
// } else {

// }

fun main() {
    val score = 65

    if (score == 100) {
        println("大吉")

    } else if (score > 80) {
        println("中吉")

    } else if (score > 60) {
        println("小吉")

    } else {
        println("吉")
    }
}

// 注意
// else if を使うときに注意するのが 「条件は上から順番に一度だけ判定される」
// 例) score が 100 だった場合
score == 100
score > 80
score > 60
// 全ての条件を満たしますが、１つ目の条件に一致した時点で２つ目以降の判定は行われない

fun main() {
    val score = 100

    if (score == 100) {  // ①ここで一致したので
        println("大吉")

    // ------ ②以下の判定は行われない ------
    } else if (score > 80) {
        println("中吉")

    } else if (score > 60) {
        println("小吉")

    } else {
        println("吉")
    }
}

// when文
// if - else if 文と同じような判定ができる when 文 という書き方もあります。
// if 文は「もし〜なら...する」というコードでしたが、when 文は「〜のときは...する」といったイメージです。
//  when {
//  	Aのときは -> ...する
//      Bのときは -> ...する
//      その他 -> ...する
//  }

fun main() {
	val score = 65
	when {
		score == 100 -> {
			println("大吉")
		}
		score > 80 -> {
			println("中吉")
		}
		score > 60 -> {
			println("小吉")
		}
		else -> {
			println("吉")
		}
	}
}

